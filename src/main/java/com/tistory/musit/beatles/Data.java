package com.tistory.musit.beatles;

public class Data {
	
	private int rank;
	private String title;
	private String albumTitle;
	private String leadVocal;
	private String primarySongWritter;
	
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAlbumTitle() {
		return albumTitle;
	}
	public void setAlbumTitle(String albumTitle) {
		this.albumTitle = albumTitle;
	}
	public String getLeadVocal() {
		return leadVocal;
	}
	public void setLeadVocal(String leadVocal) {
		this.leadVocal = leadVocal;
	}
	public String getPrimarySongWritter() {
		return primarySongWritter;
	}
	public void setPrimarySongWritter(String primarySongWritter) {
		this.primarySongWritter = primarySongWritter;
	}

}
